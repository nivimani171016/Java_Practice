
public class ArithmeticOperations_5 {

	public static void main(String[] args) {
		//adding variable 
		int x = 5;
		int y = 25;
		
		//Addition
		int sum;
		sum = x+y;
		System.out.print("The sum of two numbers is:");
		System.out.println(sum);
		
		//Subtraction 
		int diff;
		diff = y-x;
		System.out.print("The difference between two numbers is:");
		System.out.println(diff);
		
		//Multiplication
		int multi;
		multi = x*y;
		System.out.print("The multiplication of two numbers is:");
		System.out.println(multi);
		
		//Division
		float div;
		div = y/x;
		System.out.print("The division of the two numbers is:");
		System.out.println(div);
		
		//Modulo Division
		float mod;
		mod = y%x;
		System.out.print("The module division of the given number is:");
		System.out.println(mod);
		
	}
	

}
