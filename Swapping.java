
public class Swapping {

	public static void main(String[] args) {
		// To swap two numbers
		int x=5;
		int y=0,t=0;
		if (x>0)
		{
			t=x; //Value of x gets stored in the varibale
			x=y;
			y=t;
			
		}
		System.out.println("The swapped values are :");
		System.out.println(t);
		System.out.println(y);
		System.out.println(x);
	}

}
